function resolveValue(language, key) {
    return key.split('.').reduce((current, part) => current?.[part], translations[language]);
}

export function t(key, variables = {}) {
    const language = getLanguage();
    const template = resolveValue(language, key) || resolveValue('en', key) || key;

    return Object.entries(variables).reduce((result, [name, value]) => {
        return result.replaceAll(`{${name}}`, String(value));
    }, template);
}

export function getLocale() {
    return getLanguage() === 'es' ? 'es-CO' : 'en-US';
}
